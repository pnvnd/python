first_name = "Peter"
last_name = "Nguyen"

# f-strings introduced, as of Python 3.6+
print(f"Hello, {first_name} {last_name}!  Would you like to learn some Python today?")

# format() method, before Python 3.6-
print("Hello, {} {}!  Would you like to learn some Python today?".format(first_name, last_name))